    PCOST       NUMBER(8),
    AMOUNT      NUMBER(4),
    PRICE       NUMBER(8),
    PCODE       VARCHAR2(4),
    SDATE       DATE,
    CONSTRAINT FK_CUSTNO FOREIGN KEY (CUSTNO) REFERENCES MEMBER_TBL_02
);
